#include <iostream>

int main() {
    int x = 5;
    float a = 6.7f;
    const char* y = "Hello";
    std::string z = "World";
    std::cout << "int x = " << x;
    std::cout << "\nfloat a = " << a;
    std::cout << "\nchar y = " << y;
    std::cout << "\nString z = " << z << std::endl;
    return 0;
}